{
  unsigned char Temp_i;
  unsigned char Cross;  

  for(Cross=0;Cross<6;Cross++)
  {
    LCD_Set_Pos(4,7-Cross); 
    for(Temp_i=0;Temp_i<120;Temp_i++) 
    {
      if(Temp_List[Temp_i]>>3==Cross)
      {
        LCD_WrDat(Change_To_Bit[Temp_List[Temp_i]&0x07]);   
      }
      else 
      {
        LCD_WrDat(0x00);
      }     
    }    
  }
}

/*---------------------*列表移动*---------------------*/      

void List_Move(signed char New_Data)
// 对数组进行移位
{
  static signed int Temp_i;
  signed int Temp_j;
  if(Temp_i<120)
    Temp_List[Temp_i]=New_Data;
  else
  {
    for(Temp_j=0;Temp_j<119;Temp_j++)
      Temp_List[Temp_j]=Temp_List[Temp_j+1];  
    Temp_List[119]=New_Data;  
  }

  if(Temp_i>=120)
    Temp_i=120;
  else
    Temp_i++; 
}
/*-------------------*递推显示数据*-------------------*/      
   
void Paint_List(float New_Data,float Paint_Ave,float Paint_Margin)
// 将某个数据以列表刷新的形式显示出来
{
  unsigned char Temp_Data;
  
  Temp_Data=(unsigned char)(22.0/Paint_Margin*(New_Data-Paint_Ave)+26);
  List_Move(Temp_Data);
  List_Paint();
}

